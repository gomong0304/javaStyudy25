package ch03;

public class ArrayCreateExam {

	public static void main(String[] args) {
		
		int[] scores= {84,65,99,85,74,56,22,42};
		int sum=0;
		
		for(int i=0; i<6; i++) {
			sum+=scores[i];
		}// for문 종료
		System.out.println("총점 : " + sum);
		
		
		
		
		
		
		
		
		
		
		
		
	}// main 메서드 종료

}// class 종료
